#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	char *c1;
	char *c2;

	c1 = (char*)s1;
	c2 = (char*)s2;
	while (*c1 == *c2 && n--)
	{
		c1++;
		c2++;
	}
	return (*c1 - *c2);
}
